import math

"""To Calculate Area and Perimeter"""

def square_area(side):
    """This functions Returns area of square"""
    return side*side

def rectange_area(length, breadth):
    """Returns area of rectange"""
    return length*breadth

def circle(radius):
    """Returns area of circle"""
    return math.pi * r**2

def square_perimeter(side):
    """Returns perimeter of square"""
    return 4*side
